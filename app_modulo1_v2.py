    pdf.add_page()
    pdf.set_font("Arial", 'B', 10)
    pdf.cell(0, 10, "Diagn칩stico Comparativo:", ln=True)
    pdf.set_font("Arial", '', 9)
    for fila in comparativo:
        pdf.cell(0, 10, f"{fila[0]} - {fila[1]}: {fila[2]}", ln=True)
    pdf.cell(0, 10, f"\nDiagn칩stico Global: {global_diag}", ln=True)


    pdf.cell(20, 10, "Eje", 1)
    pdf.cell(40, 10, "Freq. Dom (Hz)", 1)
    pdf.cell(30, 10, "Varianza", 1)
    pdf.cell(20, 10, "RMS", 1)
    pdf.cell(50, 10, "Max. Desplaz. (cm)", 1)

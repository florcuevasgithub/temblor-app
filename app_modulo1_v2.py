    pdf.set_font("Arial", '', 8)
    for r in resultados:
        pdf.cell(30, 10, r[0], 1)
        pdf.cell(20, 10, r[1], 1)
        pdf.cell(40, 10, f"{r[2]:.2f}", 1)
        pdf.cell(30, 10, f"{r[3]:.2f}", 1)
        pdf.cell(20, 10, f"{r[4]:.2f}", 1)
        pdf.cell(50, 10, f"{r[5]:.2f}", 1)
